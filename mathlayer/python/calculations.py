def sumar_numeros(a, b):
    """
    Esta función toma dos números como argumentos y devuelve su suma.
    """
    suma = a + b
    return suma

def restar_numeros(a, b):
    """
    Esta función toma dos números como argumentos y devuelve su resta.
    """
    resta = a - b
    return resta

def multiplicar_numeros(a, b):
    """
    Esta función toma dos números como argumentos y devuelve su producto.
    """
    producto = a * b
    return producto

def dividir_numeros(a, b):
    """
    Esta función toma dos números como argumentos y devuelve su cociente.
    """
    cociente = a / b
    return cociente 
